#include <stdio.h>

/**
 * main - Prints a small portion of the alphabet (lower and uppercase),
 *        followed by a newline, demonstrating putchar usage.
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
putchar('a');	/* Prints lowercase 'a'*/
putchar('A');	/* Prints uppercase 'A'*/
putchar('\n');	/* Prints a newline */
return (0);
}
